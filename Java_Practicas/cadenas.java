public class cadenas {
    public static void main(String[] args) {

        String nombre = "Brandon Jared";
        String nombre2 = "Brandon Jared";

        System.out.println(nombre.length()); // Longitud de la cadena
        System.out.println(nombre.charAt(2)); // Letra de un indice
        System.out.println(nombre.substring(0, 7));

        System.out.println(nombre.equals(nombre2));

    }
}